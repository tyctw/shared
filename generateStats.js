function parseData(text, year) {
    const lines = text.replace(/\n100\.00\n%\n/g, ' 100.00% ').split('\n');
    const result = [];
    for(const line of lines) {
        if (line.match(/^[0-5]A[0-5]B[0-5]C/)) {
            let cleanLine = line.replace(/%/g, '');
            // handle the weird spacing
            cleanLine = cleanLine.replace(/\s+/g, ' ');
            const parts = cleanLine.split(' ');
            
            result.push({
                category: parts[0],
                year: year,
                count: parseInt(parts[1], 10),
                percentage: parseFloat(parts[2]),
                writing: {
                    6: { count: parseInt(parts[3], 10), percentage: parseFloat(parts[4]) },
                    5: { count: parseInt(parts[6], 10), percentage: parseFloat(parts[7]) },
                    4: { count: parseInt(parts[9], 10), percentage: parseFloat(parts[10]) },
                    3: { count: parseInt(parts[12], 10), percentage: parseFloat(parts[13]) }
                }
            });
        }
    }
    return result;
}

